#Built-in method input(argument)-Se utiliza para que el usuario
user_name = input("Cómo te llamas")
edad_txt = input("Cuál es tu edad")

#Built-in method type(1 argument)-Nos dice el tipo de variable
print(user_name)
print(type(user_name))

print(edad_txt)
print(type(edad_txt))

#Metodo built-in int (1-argument) - Convierte un string a un numero entero
edad = int(edad_txt)
print(edad)
print(type(edad))

